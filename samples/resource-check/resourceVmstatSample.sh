#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceVmstatSample.sh
# 概要　　　　：vmstat によるシステム統計の取得サンプル
# 用途　　　　：客先環境での vmstat による負荷傾向確認
# 説明　　　　：
#   vmstat コマンドを使用して仮想メモリ、スワップ、ブロック I/O、
#   システムアクティビティの統計を標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   vmstat が存在しない場合は「command not found」を表示してスキップする。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceVmstatSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   実行結果は環境に依存する。
#   root 権限は不要だが、一部のコマンドは権限不足でスキップされる場合がある。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_WR=1
JOB_ER=2

interval_sec=1
count=3

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceVmstatSample.sh [interval_sec] [count]

Options:
  -h : Usage を表示

Arguments:
  interval_sec : 取得間隔秒（正の整数）省略時=1
  count        : 取得回数（正の整数）省略時=3

Example:
  bash resourceVmstatSample.sh
  bash resourceVmstatSample.sh 5 12
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   interval_sec と count はそれぞれ正の整数でなければならない。
#   不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：[$1=interval_sec] [$2=count]
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_WR}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 2 ]; then
    usage
    exit ${JOB_WR}
  fi

  if [ $# -ge 1 ]; then
    if ! isPositiveInt "$1"; then
      usage
      exit ${JOB_WR}
    fi
    interval_sec="$1"
  fi

  if [ $# -ge 2 ]; then
    if ! isPositiveInt "$2"; then
      usage
      exit ${JOB_WR}
    fi
    count="$2"
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：isPositiveInt
# 概要　　　：正の整数かどうかを判定する
# 説明　　　：
#   値が 1 以上の整数である場合に 0 を返す。
#   空文字・非数値・0・負数は 1 を返す。
#
# 引数　　　：$1=判定対象の値
# 戻り値　　：0=正の整数, 1=それ以外
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
isPositiveInt() {
  local val="$1"
  case "${val}" in
    ''|*[!0-9]*)
      return 1 ;;
    0)
      return 1 ;;
    *)
      return 0 ;;
  esac
}

# ------------------------------------------------------------------
# 関数名　　：collectVmstatSnapshot
# 概要　　　：vmstat の現在スナップショットを収集して出力する
# 説明　　　：
#   vmstat コマンドで現在の仮想メモリ統計を 1 回取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectVmstatSnapshot() {
  printSection "vmstat スナップショット"
  checkCmd vmstat || return 0
  vmstat
}

# ------------------------------------------------------------------
# 関数名　　：collectVmstatInterval
# 概要　　　：vmstat のインターバル計測結果を収集して出力する
# 説明　　　：
#   vmstat を interval_sec 秒間隔で count 回取得してシステムアクティビティを表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectVmstatInterval() {
  printSection "vmstat インターバル計測 (${interval_sec}秒 × ${count}回)"
  checkCmd vmstat || return 0
  vmstat "${interval_sec}" "${count}"
}

# ------------------------------------------------------------------
# 関数名　　：collectVmstatDisk
# 概要　　　：vmstat のディスク統計を収集して出力する
# 説明　　　：
#   vmstat -d でディスクデバイスごとの I/O 統計を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectVmstatDisk() {
  printSection "vmstat ディスク統計 (vmstat -d)"
  checkCmd vmstat || return 0
  vmstat -d
}

# ------------------------------------------------------------------
# 関数名　　：collectVmstatSlabs
# 概要　　　：vmstat のスラブ情報を収集して出力する
# 説明　　　：
#   vmstat -m でカーネルスラブの使用状況を取得する。
#   環境によっては root 権限が必要な場合がある。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectVmstatSlabs() {
  printSection "vmstat スラブ情報 (vmstat -m)"
  checkCmd vmstat || return 0
  vmstat -m 2>/dev/null || printf '[INFO] vmstat -m: 権限不足または非対応\n'
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== vmstat システム統計サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectVmstatSnapshot
collectVmstatInterval
collectVmstatDisk
collectVmstatSlabs

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
