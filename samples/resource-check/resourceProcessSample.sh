#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceProcessSample.sh
# 概要　　　　：プロセス情報の取得サンプル
# 用途　　　　：客先環境でのプロセス情報取得観点確認
# 説明　　　　：
#   プロセス一覧、CPU/メモリ使用率上位プロセス、サービス状態を
#   標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   コマンドが存在しない場合は「command not found」を表示してスキップする。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceProcessSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   実行結果は環境に依存する。
#   root 権限は不要だが、一部のコマンドは権限不足でスキップされる場合がある。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_ER=2

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceProcessSample.sh

Options:
  -h : Usage を表示

Example:
  bash resourceProcessSample.sh
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   引数不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：スクリプト引数一式
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_ER}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 0 ]; then
    usage
    exit ${JOB_ER}
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：collectPsList
# 概要　　　：全プロセス一覧を収集して出力する
# 説明　　　：
#   ps auxf でプロセスのフォレスト表示を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectPsList() {
  printSection "全プロセス一覧 (ps auxf)"
  ps auxf
}

# ------------------------------------------------------------------
# 関数名　　：collectTopCpuProcess
# 概要　　　：CPU 使用率上位プロセスを収集して出力する
# 説明　　　：
#   ps コマンドで CPU 使用率の高いプロセスを上位 15 件表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectTopCpuProcess() {
  printSection "CPU 使用率上位プロセス (top 15)"
  ps aux --sort='-%cpu' | head -16
}

# ------------------------------------------------------------------
# 関数名　　：collectTopMemProcess
# 概要　　　：メモリ使用率上位プロセスを収集して出力する
# 説明　　　：
#   ps コマンドでメモリ使用率の高いプロセスを上位 15 件表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectTopMemProcess() {
  printSection "メモリ使用率上位プロセス (top 15)"
  ps aux --sort='-%mem' | head -16
}

# ------------------------------------------------------------------
# 関数名　　：collectPstree
# 概要　　　：プロセスツリーを収集して出力する
# 説明　　　：
#   pstree コマンドでプロセスの親子関係をツリー表示する。
#   pstree が存在しない場合はスキップする。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectPstree() {
  printSection "プロセスツリー (pstree -p)"
  if checkCmd pstree; then
    pstree -p
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectServices
# 概要　　　：サービス一覧を収集して出力する
# 説明　　　：
#   systemctl で起動中のサービス一覧を取得する。
#   systemctl が存在しない場合は /sbin/service または chkconfig を試みる。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectServices() {
  printSection "サービス一覧"
  if command -v systemctl > /dev/null 2>&1; then
    printf '--- systemctl list-units (type=service, state=running) ---\n'
    systemctl list-units --type=service --state=running --no-pager 2>/dev/null
    printf '\n--- systemctl list-units (type=service, state=failed) ---\n'
    systemctl list-units --type=service --state=failed --no-pager 2>/dev/null
  elif checkCmd service; then
    service --status-all 2>/dev/null
  else
    printf '[INFO] systemctl および service が存在しないためスキップ\n'
  fi
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== プロセス情報サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectPsList
collectTopCpuProcess
collectTopMemProcess
collectPstree
collectServices

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
