#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceSarSample.sh
# 概要　　　　：sar による履歴統計の取得サンプル
# 用途　　　　：客先環境での sar 利用可否および性能履歴取得観点確認
# 説明　　　　：
#   sar コマンドを使用して CPU・メモリ・ネットワーク・ディスク I/O の
#   履歴統計を標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   sar が存在しない場合は「command not found」を表示してスキップする。
#   sar は sysstat パッケージに含まれ、環境によっては未インストールの場合がある。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceSarSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   sar は取得観点の候補の一つであり、sar 専用のサンプルではない。
#   sar が未インストールの環境では取得がスキップされる。
#   実行結果は環境に依存する。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_ER=2

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceSarSample.sh

Options:
  -h : Usage を表示

Example:
  bash resourceSarSample.sh
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   引数不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：スクリプト引数一式
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_ER}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 0 ]; then
    usage
    exit ${JOB_ER}
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：collectSarCpu
# 概要　　　：sar による CPU 使用率履歴を収集して出力する
# 説明　　　：
#   sar -u で当日の CPU 使用率履歴を取得する。
#   データが存在しない場合は 1 秒間隔で 3 回のリアルタイム計測を行う。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectSarCpu() {
  printSection "CPU 使用率履歴 (sar -u)"
  checkCmd sar || return 0
  sar -u 2>/dev/null || sar -u 1 3
}

# ------------------------------------------------------------------
# 関数名　　：collectSarMem
# 概要　　　：sar によるメモリ使用率履歴を収集して出力する
# 説明　　　：
#   sar -r で当日のメモリ使用率履歴を取得する。
#   データが存在しない場合は 1 秒間隔で 3 回のリアルタイム計測を行う。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectSarMem() {
  printSection "メモリ使用率履歴 (sar -r)"
  checkCmd sar || return 0
  sar -r 2>/dev/null || sar -r 1 3
}

# ------------------------------------------------------------------
# 関数名　　：collectSarNet
# 概要　　　：sar によるネットワーク統計履歴を収集して出力する
# 説明　　　：
#   sar -n DEV で当日のネットワークインターフェース統計を取得する。
#   データが存在しない場合は 1 秒間隔で 3 回のリアルタイム計測を行う。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectSarNet() {
  printSection "ネットワーク統計履歴 (sar -n DEV)"
  checkCmd sar || return 0
  sar -n DEV 2>/dev/null || sar -n DEV 1 3
}

# ------------------------------------------------------------------
# 関数名　　：collectSarDisk
# 概要　　　：sar によるディスク I/O 履歴を収集して出力する
# 説明　　　：
#   sar -b で当日のディスク I/O 統計を取得する。
#   データが存在しない場合は 1 秒間隔で 3 回のリアルタイム計測を行う。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectSarDisk() {
  printSection "ディスク I/O 履歴 (sar -b)"
  checkCmd sar || return 0
  sar -b 2>/dev/null || sar -b 1 3
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== sar 履歴統計サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectSarCpu
collectSarMem
collectSarNet
collectSarDisk

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
