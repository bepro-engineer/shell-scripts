#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceNetworkSample.sh
# 概要　　　　：ネットワーク情報の取得サンプル
# 用途　　　　：客先環境でのネットワーク情報取得観点確認
# 説明　　　　：
#   ネットワークインターフェース情報、接続状況、統計、ルーティング情報を
#   標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   コマンドが存在しない場合は「command not found」を表示してスキップする。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceNetworkSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   実行結果は環境に依存する。
#   root 権限は不要だが、一部のコマンドは権限不足でスキップされる場合がある。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_ER=2

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceNetworkSample.sh

Options:
  -h : Usage を表示

Example:
  bash resourceNetworkSample.sh
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   引数不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：スクリプト引数一式
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_ER}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 0 ]; then
    usage
    exit ${JOB_ER}
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：collectIfInfo
# 概要　　　：ネットワークインターフェース情報を収集して出力する
# 説明　　　：
#   ip addr を優先し、存在しない場合は ifconfig を使用する。
#   どちらも存在しない場合は /proc/net/if_inet6 で代替表示を試みる。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectIfInfo() {
  printSection "ネットワークインターフェース情報"
  if command -v ip > /dev/null 2>&1; then
    ip addr
  elif checkCmd ifconfig; then
    ifconfig -a
  elif [ -f /proc/net/dev ]; then
    printf '--- /proc/net/dev ---\n'
    cat /proc/net/dev
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectNetConnections
# 概要　　　：ネットワーク接続状況を収集して出力する
# 説明　　　：
#   ss を優先し、存在しない場合は netstat を使用する。
#   LISTEN 状態のソケットと確立済み接続を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectNetConnections() {
  printSection "ネットワーク接続状況 (LISTEN + ESTABLISHED)"
  if command -v ss > /dev/null 2>&1; then
    printf '--- ss -tulnp ---\n'
    ss -tulnp
    printf '\n--- ss -s (サマリー) ---\n'
    ss -s
  elif checkCmd netstat; then
    netstat -tulnp
  else
    printf '[INFO] ss および netstat が存在しないためスキップ\n'
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectNetStats
# 概要　　　：ネットワークインターフェース統計を収集して出力する
# 説明　　　：
#   /proc/net/dev からインターフェースごとの送受信統計を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectNetStats() {
  printSection "インターフェース送受信統計 (/proc/net/dev)"
  if [ -f /proc/net/dev ]; then
    cat /proc/net/dev
  else
    printf '[INFO] /proc/net/dev が存在しないためスキップ\n'
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectRouteInfo
# 概要　　　：ルーティング情報を収集して出力する
# 説明　　　：
#   ip route を優先し、存在しない場合は netstat -r を使用する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectRouteInfo() {
  printSection "ルーティング情報"
  if command -v ip > /dev/null 2>&1; then
    ip route
  elif checkCmd netstat; then
    netstat -r
  else
    if [ -f /proc/net/route ]; then
      printf '--- /proc/net/route ---\n'
      cat /proc/net/route
    fi
  fi
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== ネットワーク情報サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectIfInfo
collectNetConnections
collectNetStats
collectRouteInfo

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
