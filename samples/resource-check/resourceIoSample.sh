#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceIoSample.sh
# 概要　　　　：ディスク I/O 統計の取得サンプル
# 用途　　　　：客先環境での I/O 系取得観点確認
# 説明　　　　：
#   ディスク使用量、ブロックデバイス一覧、I/O 統計を標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   iostat が存在しない場合は /proc/diskstats での代替取得を試みる。
#   コマンドが存在しない場合は「command not found」を表示してスキップする。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceIoSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   実行結果は環境に依存する。
#   root 権限は不要だが、一部のコマンドは権限不足でスキップされる場合がある。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_ER=2

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceIoSample.sh

Options:
  -h : Usage を表示

Example:
  bash resourceIoSample.sh
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   引数不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：スクリプト引数一式
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_ER}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 0 ]; then
    usage
    exit ${JOB_ER}
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：collectDfInfo
# 概要　　　：ディスク使用量を収集して出力する
# 説明　　　：
#   df コマンドでマウント済みファイルシステムの使用量を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectDfInfo() {
  printSection "ディスク使用量 (df -h)"
  df -h
}

# ------------------------------------------------------------------
# 関数名　　：collectBlockDev
# 概要　　　：ブロックデバイス一覧を収集して出力する
# 説明　　　：
#   lsblk コマンドでブロックデバイスのツリー構造を取得する。
#   存在しない場合は /proc/partitions を代替として表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectBlockDev() {
  printSection "ブロックデバイス一覧"
  if checkCmd lsblk; then
    lsblk
  elif [ -f /proc/partitions ]; then
    printf '--- /proc/partitions ---\n'
    cat /proc/partitions
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectIoStat
# 概要　　　：I/O 統計を収集して出力する
# 説明　　　：
#   iostat コマンドで拡張 I/O 統計を取得する。
#   iostat が存在しない場合は /proc/diskstats を代替として表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectIoStat() {
  printSection "I/O 統計"
  if checkCmd iostat; then
    iostat -x
  else
    if [ -f /proc/diskstats ]; then
      printf '--- /proc/diskstats (iostat 代替) ---\n'
      cat /proc/diskstats
    fi
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectIoStatInterval
# 概要　　　：I/O 統計のインターバル計測結果を収集して出力する
# 説明　　　：
#   iostat を 1 秒間隔で 3 回取得してデバイスごとの I/O 傾向を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectIoStatInterval() {
  printSection "I/O 統計 インターバル計測 (1秒 × 3回)"
  if checkCmd iostat; then
    iostat -x 1 3
  else
    printf '[INFO] iostat が存在しないためスキップ\n'
  fi
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== ディスク I/O 統計サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectDfInfo
collectBlockDev
collectIoStat
collectIoStatInterval

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
