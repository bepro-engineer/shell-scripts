#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceCurrentSample.sh
# 概要　　　　：現在のリソース使用状況の取得サンプル
# 用途　　　　：客先環境での現在リソース使用状況確認
# 説明　　　　：
#   CPU 負荷、メモリ使用量、上位プロセスの現在スナップショットを標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   コマンドが存在しない場合は「command not found」を表示してスキップする。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceCurrentSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   実行結果は環境に依存する。
#   root 権限は不要だが、一部のコマンドは権限不足でスキップされる場合がある。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_ER=2

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceCurrentSample.sh

Options:
  -h : Usage を表示

Example:
  bash resourceCurrentSample.sh
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   引数不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：スクリプト引数一式
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_ER}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 0 ]; then
    usage
    exit ${JOB_ER}
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：collectLoadInfo
# 概要　　　：ロードアベレージを収集して出力する
# 説明　　　：
#   uptime コマンドおよび /proc/loadavg からロードアベレージを取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectLoadInfo() {
  printSection "ロードアベレージ"
  printf 'uptime    : %s\n' "$(uptime)"

  if [ -f /proc/loadavg ]; then
    printf 'loadavg   : %s\n' "$(cat /proc/loadavg)"
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectCpuUsage
# 概要　　　：CPU 使用状況を収集して出力する
# 説明　　　：
#   top コマンドのバッチモードで CPU 使用率を取得する。
#   top が存在しない場合はスキップする。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectCpuUsage() {
  printSection "CPU 使用状況 (top バッチモード)"
  if checkCmd top; then
    top -b -n 1 | head -20
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectMemUsage
# 概要　　　：メモリ使用状況を収集して出力する
# 説明　　　：
#   free コマンドで現在のメモリおよびスワップ使用量を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectMemUsage() {
  printSection "メモリ使用状況"
  if checkCmd free; then
    free -h
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectTopCpuProcess
# 概要　　　：CPU 使用率上位プロセスを収集して出力する
# 説明　　　：
#   ps コマンドで CPU 使用率の高いプロセスを上位 15 件表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectTopCpuProcess() {
  printSection "CPU 使用率上位プロセス (top 15)"
  ps aux --sort='-%cpu' | head -16
}

# ------------------------------------------------------------------
# 関数名　　：collectTopMemProcess
# 概要　　　：メモリ使用率上位プロセスを収集して出力する
# 説明　　　：
#   ps コマンドでメモリ使用率の高いプロセスを上位 15 件表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectTopMemProcess() {
  printSection "メモリ使用率上位プロセス (top 15)"
  ps aux --sort='-%mem' | head -16
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== 現在リソース使用状況サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectLoadInfo
collectCpuUsage
collectMemUsage
collectTopCpuProcess
collectTopMemProcess

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
