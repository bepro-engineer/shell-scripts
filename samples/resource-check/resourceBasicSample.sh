#!/bin/bash
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# スクリプト名　：resourceBasicSample.sh
# 概要　　　　：サーバー基本情報の取得サンプル
# 用途　　　　：客先環境での基本情報取得観点確認
# 説明　　　　：
#   ホスト名、OS バージョン、稼働時間、CPU コア数、メモリ総量、
#   ディスク使用状況などの基本情報を標準出力する。
#   本番用ではなく、取得観点の確認を目的としたサンプルである。
#   コマンドが存在しない場合は「command not found」を表示してスキップする。
#
# 引数　　　　：
#   -h : Usage を表示
#
# 戻り値　　　：0=正常, 2=異常
# 実行例　　　：bash samples/resource-check/resourceBasicSample.sh
# 注意事項　　：
#   本番用ではなく、客先確認目的のサンプルである。
#   実行結果は環境に依存する。
#   root 権限は不要だが、一部のコマンドは権限不足でスキップされる場合がある。
# 使用箇所　　：取得観点確認用サンプル実行時
#
# 設計書　　　：なし
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/
# ＜変更履歴＞
# Ver. 変更管理No. 日付        更新者       変更内容
# 1.0  -           2026-06-21  -            初版作成
#_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/_/

# ----------------------------------------------------------
# variables
# ----------------------------------------------------------
JOB_OK=0
JOB_ER=2

# ----------------------------------------------------------
# functions
# ----------------------------------------------------------

# ------------------------------------------------------------------
# 関数名　　：terminate
# 概要　　　：終了時の後処理を行う
# 説明　　　：
#   trap から呼び出される。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：trap
# ------------------------------------------------------------------
terminate() {
  :
}

# ------------------------------------------------------------------
# 関数名　　：usage
# 概要　　　：使用方法を標準エラー出力する
# 説明　　　：
#   このスクリプトの使い方を表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：checkArgs
# ------------------------------------------------------------------
usage() {
  cat >&2 <<'EOF'
--------------------------------------
Usage:
  bash resourceBasicSample.sh

Options:
  -h : Usage を表示

Example:
  bash resourceBasicSample.sh
--------------------------------------
EOF
}

# ------------------------------------------------------------------
# 関数名　　：checkArgs
# 概要　　　：引数の妥当性を確認する
# 説明　　　：
#   引数不正を検知した場合は Usage を表示して終了する。
#
# 引数　　　：スクリプト引数一式
# 戻り値　　：なし（エラー時はスクリプトを終了する）
# 使用箇所　：前処理
# ------------------------------------------------------------------
checkArgs() {
  while getopts ":h" opt; do
    case "${opt}" in
      h)
        usage
        exit ${JOB_OK}
        ;;
      \?)
        usage
        exit ${JOB_ER}
        ;;
    esac
  done
  shift $((OPTIND - 1))

  if [ $# -gt 0 ]; then
    usage
    exit ${JOB_ER}
  fi
}

# ------------------------------------------------------------------
# 関数名　　：checkCmd
# 概要　　　：コマンドの存在を確認する
# 説明　　　：
#   指定コマンドが使用可能かどうかを確認する。
#   存在しない場合は「command not found」を表示して 1 を返す。
#
# 引数　　　：$1=確認対象コマンド名
# 戻り値　　：0=存在する, 1=存在しない
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
checkCmd() {
  local cmd="$1"
  if ! command -v "${cmd}" > /dev/null 2>&1; then
    printf '[INFO] command not found: %s\n' "${cmd}"
    return 1
  fi
  return 0
}

# ------------------------------------------------------------------
# 関数名　　：printSection
# 概要　　　：セクション区切りを出力する
# 説明　　　：
#   セクションタイトルを見やすい形式で出力する。
#
# 引数　　　：$1=セクションタイトル
# 戻り値　　：なし
# 使用箇所　：各取得関数
# ------------------------------------------------------------------
printSection() {
  local title="$1"
  printf '\n=== %s ===\n' "${title}"
}

# ------------------------------------------------------------------
# 関数名　　：collectOsInfo
# 概要　　　：OS 基本情報を収集して出力する
# 説明　　　：
#   ホスト名、カーネルバージョン、OS リリース情報、稼働時間を取得する。
#   /etc/os-release が存在する場合は OS 名とバージョンも表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectOsInfo() {
  printSection "OS 基本情報"
  printf 'hostname : %s\n' "$(hostname)"
  printf 'uname -a : %s\n' "$(uname -a)"
  printf 'uptime   : %s\n' "$(uptime)"

  if [ -f /etc/os-release ]; then
    printf '\n--- /etc/os-release ---\n'
    grep -E '^(NAME|VERSION|ID|PRETTY_NAME)=' /etc/os-release
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectCpuInfo
# 概要　　　：CPU 情報を収集して出力する
# 説明　　　：
#   CPU コア数と /proc/cpuinfo からモデル名を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectCpuInfo() {
  printSection "CPU 情報"
  printf 'CPU cores : %s\n' "$(nproc 2>/dev/null || grep -c '^processor' /proc/cpuinfo 2>/dev/null || printf 'unknown')"
  if [ -f /proc/cpuinfo ]; then
    printf 'CPU model : %s\n' "$(grep 'model name' /proc/cpuinfo | head -1 | cut -d':' -f2 | sed 's/^ *//')"
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectMemInfo
# 概要　　　：メモリ情報を収集して出力する
# 説明　　　：
#   free コマンドでメモリおよびスワップの使用状況を取得する。
#   /proc/meminfo の主要項目も表示する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectMemInfo() {
  printSection "メモリ情報"
  if checkCmd free; then
    free -h
  fi

  if [ -f /proc/meminfo ]; then
    printf '\n--- /proc/meminfo (主要項目) ---\n'
    grep -E '^(MemTotal|MemFree|MemAvailable|SwapTotal|SwapFree):' /proc/meminfo
  fi
}

# ------------------------------------------------------------------
# 関数名　　：collectDiskInfo
# 概要　　　：ディスク使用状況を収集して出力する
# 説明　　　：
#   df コマンドでマウント済みファイルシステムの使用状況を取得する。
#
# 引数　　　：なし
# 戻り値　　：なし
# 使用箇所　：メイン処理
# ------------------------------------------------------------------
collectDiskInfo() {
  printSection "ディスク使用状況"
  df -h
}

# ----------------------------------------------------------
# pre-process
# ----------------------------------------------------------
trap "terminate" HUP INT QUIT TERM

checkArgs "$@"

# ----------------------------------------------------------
# main-routine
# ----------------------------------------------------------
printf '=== サーバー基本情報サンプル ===\n'
printf '取得日時 : %s\n' "$(date '+%Y-%m-%d %H:%M:%S')"

collectOsInfo
collectCpuInfo
collectMemInfo
collectDiskInfo

# ----------------------------------------------------------
# post-process
# ----------------------------------------------------------
exit ${JOB_OK}
